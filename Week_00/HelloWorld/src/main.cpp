// We include the <iostream> functions to make sure our program knows how to
// output text to the console.
#include <iostream>

int main()
{
    // Here we send the "Hello World!" text to the console, and follow it with
    // a new line \n character std::endl.
    std::cout << "Hello world!" << std::endl;

    // We return a 0, meaning that the program finished without any problems.
    return 0;
}
